	<script type="text/javascript">

		var $slider = $('.slider-banner');
		var $progressBar = $('.progress');
	  var $status = $('.pagingInfo');
	  var $progressBarLabel = $( '.slider__label' );

		$slider.on('init reInit afterChange', function (event, slick, currentSlide, nextSlide) {
		  //currentSlide is undefined on init -- set it to 0 in this case (currentSlide is 0 based)
		  if(!slick.$dots){
		  	return;
		  }
		  
		  // if(currentSlide >= 4){
		  //   $('#exampleModal').modal('show');    
		  //   setTimeout(function(){
		  //     $slider.slick('slickGoTo',3,true)
		  //   },1)    
		  // }
		  
		  var i = (currentSlide ? currentSlide : 0) + 1;
		  $status.text(i  + '/' + slick.slideCount);
		});

		  //   Progress Bar
		  $slider.on('beforeChange', function(event, slick, currentSlide, nextSlide) {   
		    var calc = ( (nextSlide) / (slick.slideCount-1) ) * 100;
		    
		    $progressBar
		      .css('background-size', calc + '% 100%')
		      .attr('aria-valuenow', calc );
		    
		    $progressBarLabel.text( calc + '% completed' );
		  });

		$('.slider-banner').slick({
		    // arrows: false,
		    dots: true,
		    slidesToShow: 1,
		    slidesToScroll: 1,
		    prevArrow: "<button class='arrowBannerLeft'><i class='fas fa-chevron-left'></i></button>",
		    nextArrow: "<button class='arrowBannerRight'><i class='fas fa-chevron-right'></i></button>",
		});

		$(document).ready(function(){
			$('.rtl-slider').slick({
			  slidesToShow: 1,
			  slidesToScroll: 1,
			  arrows: false,
			  fade: true,
			  asNavFor: '.rtl-slider-nav'
			});
			$('.rtl-slider-nav').slick({
				slidesToShow: 4,
				slidesToScroll: 1,
				vertical: true,
			   asNavFor: '.rtl-slider',
			   centerMode: false,
			   focusOnSelect: true,
				prevArrow: ".thumb-prev",
		   	nextArrow: ".thumb-next",
			});
		});

		const tabs = document.querySelectorAll('.tabs__btn');
	    const tabsContent = document.querySelectorAll('.tabs__body');

	    if (tabsContent.length > 0 || tabs.length > 0) {

	        function hideTabContent() {
	            tabsContent.forEach(item => {
	                item.classList.remove('active');
	            });

	            tabs.forEach(item => {
	                item.classList.remove('active');
	            });
	        }

	        function showTabContent(i = 0) {
	            tabsContent[i].classList.add('active');
	            tabs[i].classList.add('active');
	        }

	        hideTabContent();
	        showTabContent();

	        tabs.forEach((tab, index) => {
	            tab.addEventListener('click', () => {
	                hideTabContent();
	                showTabContent(index);
	            });
	        });
	    }

 	</script>